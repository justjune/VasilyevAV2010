// Пример использования переменной
#include <iostream>
using namespace std;
int main(){
int age;                                      // Переменная для записи возраста
cout << "How old are You?\n";                 // Сколько Вам лет?
cin >> age;                                   // Нужно указать возраст
cout << "You are " << age << " years old\n";  // Результат
return 0;
}